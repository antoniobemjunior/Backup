package beans;

public class Estatistica {

	//Atributos
	private String nome;
	private int qtd;
	
	//Set & Get
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public int getQtd() {
		return qtd;
	}
	public void setQtd(int qtd) {
		this.qtd = qtd;
	}
	
	
	
}
