package beans;

public class EstatisticasPlataforma {

	//Atributos
	private String plataformaJogo;
	private int qtdPlataformaJogo;
		
	//Get & Set
	public String getPlataformaJogo() {
		return plataformaJogo;
	}
	public void setPlataformaJogo(String plataformaJogo) {
		this.plataformaJogo = plataformaJogo;
	}
	public int getQtdPlataformaJogo() {
		return qtdPlataformaJogo;
	}
	public void setQtdPlataformaJogo(int qtdPlataformaJogo) {
		this.qtdPlataformaJogo = qtdPlataformaJogo;
	}
}
