package beans;

public class EstatisticasGenero {

	//Atributos
	private String generoJogo;
	private int qtdGeneroJogo;
	
	//Get e Set
	public String getGeneroJogo() {
		return generoJogo;
	}
	public void setGeneroJogo(String generoJogo) {
		this.generoJogo = generoJogo;
	}
	public int getQtdGeneroJogo() {
		return qtdGeneroJogo;
	}
	public void setQtdGeneroJogo(int qtdGeneroJogo) {
		this.qtdGeneroJogo = qtdGeneroJogo;
	}
}
