package exercicio_1;

public class Jogo {

	private String nome_jogo;
	private String genero_jogo;
	private String plataforma_jogo;
	private int classificacao_jogo;
	private double valor_jogo;
	
	public String getNome_jogo() {
		return nome_jogo;
	}
	public void setNome_jogo(String nome_jogo) {
		this.nome_jogo = nome_jogo;
	}
	public String getGenero_jogo() {
		return genero_jogo;
	}
	public void setGenero_jogo(String genero_jogo) {
		this.genero_jogo = genero_jogo;
	}
	public String getPlataforma_jogo() {
		return plataforma_jogo;
	}
	public void setPlataforma_jogo(String plataforma_jogo) {
		this.plataforma_jogo = plataforma_jogo;
	}
	public int getClassificacao_jogo() {
		return classificacao_jogo;
	}
	public void setClassificacao_jogo(int classificacao_jogo) {
		this.classificacao_jogo = classificacao_jogo;
	}
	public double getValor_jogo() {
		return valor_jogo;
	}
	public void setValor_jogo(double valor_jogo) {
		this.valor_jogo = valor_jogo;
	}
	
	
}
